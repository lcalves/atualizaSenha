package br.com.leonardo.to;


/**
 * classe que representa o id do usuário e o password
 * @author Leonardo
 *
 */
public class Password {
	
	private int id;
	private String senha;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getSenha() {
		return senha;
	}
	public void setSenha(String senha) {
		this.senha = senha;
	}
	
	

}
